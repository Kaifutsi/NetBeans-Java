    private void jbtnAjaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAjaActionPerformed
        // TODO add your handling code here:
        String teksti1 = jtxt1.getText();
        String teksti2 = jtxt2.getText();
        
        jrbtVertaaminen.setActionCommand("Vertaaminen");
        jrbtPituus.setActionCommand("Pituus");
        jrbtSuuri.setActionCommand("Suuri");
        jrbtLiite.setActionCommand("Liite");
        jrbtK.setActionCommand("K");
        jrbtKorvaus.setActionCommand("Korvaus");
        jrbtTaulukko.setActionCommand("Taulukko");
        
        
        if(buttonGroup1.getSelection().getActionCommand()=="Vertaaminen") {
            if(teksti1.equals(teksti2)) {
                dm.addElement("teksti1 on yht채 kuin teksti2");
            } else {
                dm.addElement("teksti1 ei ole yht채 kuin teksti2");
            }
            jtxtareaTulos.setModel(dm);
        }
        
        if(buttonGroup1.getSelection().getActionCommand()=="Pituus") {
            int pituus=0;
            for(int i = 0; i <= teksti1.length(); i++) {
                pituus = i;
                
            }
            dm.addElement("Teksti1, pituus: " + pituus);
            jtxtareaTulos.setModel(dm);
        }
        
        if(buttonGroup1.getSelection().getActionCommand()=="Suuri") {
            dm.addElement(teksti2.toUpperCase());
            jtxtareaTulos.setModel(dm);
        }
        
        if(buttonGroup1.getSelection().getActionCommand()=="Liite") {
            dm.addElement(teksti1.toLowerCase() + " " + teksti2.toUpperCase());
            jtxtareaTulos.setModel(dm);
        }
        
        if(buttonGroup1.getSelection().getActionCommand()=="K") {
            char t = teksti2.charAt(0);  
            if(t == 'K') {
                String lause = "Kyll채";
                dm.addElement(lause);  
            }else{
                dm.addElement("Ei");  
            }
            jtxtareaTulos.setModel(dm);
        }
        
        if(buttonGroup1.getSelection().getActionCommand()=="Korvaus") {  
            String t = jtxt1.getText();
            String vaikea = "vaikea";
            dm.addElement(t.replace("vaikea", "helppo"));
            jtxtareaTulos.setModel(dm);
        }   
        
        if(buttonGroup1.getSelection().getActionCommand()=="Taulukko") {
            String t = new String(jtxt1.getText());
            String[] words = t.split(",");
            int i = 0;
            for(String word: words) {
                String sana = word + " : " + i;
                i++;
                dm.addElement(sana);
            }
            jtxtareaTulos.setModel((dm));

        }          
    }//GEN-LAST:event_jbtnAjaActionPerformed

